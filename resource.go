// the config files usually given to envoy are a Bootstrap. Load one from the filesystem and use it to populate the Snapshot.
func ReadConfig(filepath string) (*bootstrapv3.Bootstrap, error) {
	f, err := os.Open(filepath)
	if err != nil {
		return nil, err
	}
	fb, err := io.ReadAll(f)
	log.Printf("config bytes: \n%s\n", string(fb))
	if err != nil {
		return nil, err
	}
	var bsc bootstrapv3.Bootstrap
	err = protoyaml.Unmarshal(fb, &bsc)
	if err != nil {
		return nil, err
	}
	log.Printf("%#v\n", bsc.GetStaticResources())
	return &bsc, nil
}

func makeCluster(clusterName string) *cluster.Cluster {
	routerConfig, _ := anypb.New(&router.Router{})
	return &cluster.Cluster{
		Name:                 clusterName,
		ConnectTimeout:       durationpb.New(5 * time.Second),
		ClusterDiscoveryType: &cluster.Cluster_Type{Type: cluster.Cluster_LOGICAL_DNS},
		LbPolicy:             cluster.Cluster_ROUND_ROBIN,
		LoadAssignment:       makeEndpoint(clusterName),
		DnsLookupFamily:      cluster.Cluster_V4_ONLY,
		Filters: []*cluster.Filter{
			{
				Name:        "http-router",
				TypedConfig: routerConfig,
			},
		},
	}
}

func makeEndpoint(clusterName string) *endpoint.ClusterLoadAssignment {
	return &endpoint.ClusterLoadAssignment{
		ClusterName: clusterName,
		Endpoints: []*endpoint.LocalityLbEndpoints{{
			LbEndpoints: []*endpoint.LbEndpoint{{
				HostIdentifier: &endpoint.LbEndpoint_Endpoint{
					Endpoint: &endpoint.Endpoint{
						Address: &core.Address{
							Address: &core.Address_SocketAddress{
								SocketAddress: &core.SocketAddress{
									Protocol: core.SocketAddress_TCP,
									Address:  UpstreamHost,
									PortSpecifier: &core.SocketAddress_PortValue{
										PortValue: UpstreamPort,
									},
								},
							},
						},
					},
				},
			}},
		}},
	}
}

func makeRoute(routeName, clusterName string) *route.RouteConfiguration {
	return &route.RouteConfiguration{
		Name: routeName,
		VirtualHosts: []*route.VirtualHost{{
			Name:    "local_service",
			Domains: []string{"*"},
			Routes: []*route.Route{{
				Match: &route.RouteMatch{
					PathSpecifier: &route.RouteMatch_Prefix{
						Prefix: "/",
					},
				},
				Action: &route.Route_Route{
					Route: &route.RouteAction{
						ClusterSpecifier: &route.RouteAction_Cluster{
							Cluster: clusterName,
						},
						HostRewriteSpecifier: &route.RouteAction_HostRewriteLiteral{
							HostRewriteLiteral: UpstreamHost,
						},
					},
				},
			}},
		}},
	}
}

func makeHTTPListener(listenerName, route string) *listener.Listener {
	routerConfig, _ := anypb.New(&router.Router{})
	// HTTP filter configuration
	manager := &hcm.HttpConnectionManager{
		CodecType:  hcm.HttpConnectionManager_AUTO,
		StatPrefix: "http",
		RouteSpecifier: &hcm.HttpConnectionManager_Rds{
			Rds: &hcm.Rds{
				ConfigSource:    makeConfigSource(),
				RouteConfigName: route,
			},
		},
		HttpFilters: []*hcm.HttpFilter{{
			Name:       "http-router",
			ConfigType: &hcm.HttpFilter_TypedConfig{TypedConfig: routerConfig},
		}},
	}
	pbst, err := anypb.New(manager)
	if err != nil {
		panic(err)
	}

	return &listener.Listener{
		Name: listenerName,
		Address: &core.Address{
			Address: &core.Address_SocketAddress{
				SocketAddress: &core.SocketAddress{
					Protocol: core.SocketAddress_TCP,
					Address:  "0.0.0.0",
					PortSpecifier: &core.SocketAddress_PortValue{
						PortValue: ListenerPort,
					},
				},
			},
		},
		FilterChains: []*listener.FilterChain{{
			Filters: []*listener.Filter{{
				Name: "http-connection-manager",
				ConfigType: &listener.Filter_TypedConfig{
					TypedConfig: pbst,
				},
			}},
		}},
	}
}

func makeConfigSource() *core.ConfigSource {
	source := &core.ConfigSource{}
	source.ResourceApiVersion = resource.DefaultAPIVersion
	source.ConfigSourceSpecifier = &core.ConfigSource_ApiConfigSource{
		ApiConfigSource: &core.ApiConfigSource{
			TransportApiVersion:       resource.DefaultAPIVersion,
			ApiType:                   core.ApiConfigSource_GRPC,
			SetNodeOnFirstMessageOnly: true,
			GrpcServices: []*core.GrpcService{{
				TargetSpecifier: &core.GrpcService_EnvoyGrpc_{
					EnvoyGrpc: &core.GrpcService_EnvoyGrpc{ClusterName: "xds_cluster"},
				},
			}},
		},
	}
	return source
}

func GenerateSnapshot(bc *bootstrapv3.Bootstrap) *cache.Snapshot {
	var clusters []types.Resource
	var listeners []types.Resource
	var endpoints []types.Resource
	for _, c := range bc.GetStaticResources().GetClusters() {
		clusters = append(clusters, c)
		endpoints = append(endpoints, c.GetLoadAssignment())
	}
	for _, l := range bc.GetStaticResources().GetListeners() {
		listeners = append(listeners, l)
	}
	log.Printf("Clusters: %s", clusters)
	log.Printf("Endpoints: %s", endpoints)
	log.Printf("Listeners: %s", listeners)
	snap, _ := cache.NewSnapshot("1",
		map[resource.Type][]types.Resource{
			resource.ClusterType: clusters,
			// resource.RouteType:    {bc.},
			resource.ListenerType: listeners,
			resource.EndpointType: endpoints,
		},
	)
	refs := cache.GetAllResourceReferences(snap.Resources)
	log.Printf("References: %#v\n", refs[resource.EndpointType])
	res := snap.GetResources(resource.EndpointType)
	log.Printf("Resources: %#v\n", res)
	return snap
}
